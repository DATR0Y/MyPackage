package mymath

import "math"

func Sqrt(x float64) float64 {
	return math.Sqrt(x)
}
func Ceil(num float64) float64 {
	return math.Ceil(num)
}
func Floor(num float64) float64 {
	return math.Floor(num)
}
func Pow(num, po float64) float64 {
	return math.Pow(num, po)
}
func Max(ma, mi float64) float64 {
	return math.Max(ma, mi)
}
func Min(ma, mi float64) float64 {
	return math.Min(ma, mi)
}
func Abs(num float64) float64 {
	return math.Abs(num)
}
func Yn(num int, secnum float64) float64 {
	return math.Yn(num, secnum)
}
func Acos(x float64) float64 {
	return math.Acos(x)
}
func Acosh(x float64) float64 {
	return math.Acosh(x)
}
func Asin(x float64) float64 {
	return math.Asin(x)
}
